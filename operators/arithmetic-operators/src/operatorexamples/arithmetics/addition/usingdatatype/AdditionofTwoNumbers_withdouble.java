package operatorexamples.arithmetics.addition.usingdatatype;

public class AdditionofTwoNumbers_withdouble {

    public static void main(String[] args) {

        double num1 = 132.55d;
        double num2 = 254.44d;

        double sum = num1 + num2;

        System.out.println("Sum of " + num1 + " + " + num2 + " = " + sum);
    }
}
