package operatorexamples.arithmetics.addition.usingdatatype;

public class AdditionofTwoNumber_withInt {

    // Question: WAP to add two numbers using int data type and print the result?
    // Pseudocode: (steps)
    // Step 1: Declare and initialize two integer variables with values
    // Step 2: Add the two integers and
    // Step 3: Store the result in a third variable
    // Step 4: Print the result

    public static void main(String[] args) {

        // Step 1: Declare and initialize two integer numbers
        int num1 = 10;
        int num2 = 20;

        // Step 2: Add and Calculate the sum of the two numbers
        int sum = num1 + num2; // Step 3: Store in third variable 'sum'

        // Step 3: Print the result
        System.out.println("Sum of " + num1 + " + " + num2 + " = " + sum);
    }
}
