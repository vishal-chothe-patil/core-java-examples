package operatorexamples.arithmetics.addition.usingdatatype;

public class AdditionofTwoNumber_withfloat {

    public static void main(String[] args) {

        float num1 = 13.5f;
        float num2 = 25.4f;

        float sum = num1 + num2;

        System.out.println("Sum of " + num1 + " + " + num2 + " = " + sum);
    }
}
