package operatorexamples.arithmetics.multiplication.usingdatatype;

public class MultiplicationofTwoNumber_withdouble {

    public static void main(String[] args) {

        double val1 = 133.5d;
        double val2 = 10.5d;

        double result = val1 * val2;

        System.out.println("Multiplication of " + val1 + " * " + val2 + " = " + result);
    }
}
