package operatorexamples.arithmetics.multiplication.usingdatatype;

public class MultiplicationofTwoNumber_withshort {

    public static void main(String[] args) {

        short val1 = 16;
        short val2 = 5;

        int result = val1 * val2;

        System.out.println("Multiplication of " + val1 + " * " + val2 + " = " + result);
    }
}
