package operatorexamples.arithmetics.multiplication.usingdatatype;

public class MultiplicationofTwoNumber_withfloat {

    public static void main(String[] args) {

        float val1 = 13.5f;
        float val2 = 10.6f;

        float result = val1 * val2;

        System.out.println("Multiplication of " + val1 + " * " + val2 + " = " + result);
    }
}
