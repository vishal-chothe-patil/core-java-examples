package operatorexamples.arithmetics.subtraction.usingdatatype;

public class SubtractionofTwoNumber_withlong {

    public static void main(String[] args) {

        long val1 = 5656L;
        long val2 = 2525L;

        long result = val1 - val2;

        System.out.println("Subtraction of " + val1 + " - " + val2 + " = " + result);
    }
}
