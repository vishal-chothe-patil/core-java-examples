package operatorexamples.arithmetics.subtraction.usingdatatype;

public class SubtractionofTwoNumber_withdouble {

    public static void main(String[] args) {

        double val1 = 153.5;
        double val2 = 84.3;

        double result = val1 - val2;

        System.out.println("Subtraction of " + val1 + " - " + val2 + " = " + result);
    }
}
