package operatorexamples.division.usingdatatype;

public class DivisionofTwoNumber_withdouble {

    public static void main(String[] args) {

        double val1 = 555.5d;
        double val2 = 5.5d;

        double result = val1 / val2;

        System.out.println("Division of " + val1 + " / " + val2 + " = " + result);
    }
}
