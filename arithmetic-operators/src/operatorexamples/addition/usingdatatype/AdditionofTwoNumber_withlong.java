package operatorexamples.addition.usingdatatype;

public class AdditionofTwoNumber_withlong {

    public static void main(String[] args) {

        long num1 = 103L;
        long num2 = 205L;

        long sum = num1 + num2;

        System.out.println("Sum of " + num1 + " + " + num2 + " = " + sum);
    }
}
