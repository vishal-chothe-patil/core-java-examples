package operatorexamples.subtraction.usingdatatype;

public class SubtractionofTwoNumber_withInt {

    public static void main(String[] args) {

        int val1 = 56;
        int val2 = 25;

        int result = val1 - val2;

        System.out.println("Subtraction of " + val1 + " - " + val2 + " = " + result);
    }
}
