public class DesignPatternExamples {
    public static void main(String[] args) {
        System.out.println("Hello and welcome to Java Design Pattern!");

        String[] patterns = {
                "1. Simple (Direct)",
                "2. Factory Pattern",
                "3. Singleton Pattern",
                "4. Builder Pattern",
                "5. Prototype Pattern"
        };

        for (String pattern : patterns) {
            System.out.println(pattern);
        }
    }
}
